function getFIO(clients) {
  return clients.surname +" "+ clients.name +" "+ clients.lastname;
}

function getDateOfCreationString(clients) {
  const yyyy = clients.dateOfCreation.getFullYear();
  let mm = clients.dateOfCreation.getMonth() + 1;
  let dd = clients.dateOfCreation.getDate();
  let hh = clients.dateOfCreation.getHours();
  let min = clients.dateOfCreation.getMinutes();
  if(dd<10) dd = "0" + dd;
  if(mm<10) mm = "0" + mm;
  if(hh<10) hh = "0" + hh;
  if(min<10) min = "0" + min;

  const today = dd+"."+mm+"."+yyyy+" "+hh+":"+min;

  return today;
}

