function createContact(contactObj) {
  let type;
  switch (contactObj.type) {
    case 'Email':
      type = 'email';
      break;
    case 'Телефон':
      type = 'tel';
      break;
    case 'Facebook':
      type = 'fb';
      break;
    case 'Вконтакте':
      type = 'vk';
      break
  }
  const triggerWrapper = document.createElement('span');
  const contactBtn = document.createElement('button');
  contactBtn.innerHTML = svgs[type];
  contactBtn.setAttribute('aria-expanded', false);
  contactBtn.classList.add("btn-contact");
  
  const tippyinst = tippy(contactBtn, {
    content: `<span> <a style="color: white"><b >${contactObj.value}</b></a></span>`,
    allowHTML: true,
    interactive: true,
    appendTo:triggerWrapper,
  })

  tippyinst.popper.querySelector('a').addEventListener('blur', ()=>tippyinst.hide())
  
  triggerWrapper.append(contactBtn)
  return triggerWrapper;
}

